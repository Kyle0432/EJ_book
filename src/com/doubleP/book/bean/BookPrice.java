package com.doubleP.book.bean;

/**
 * 书籍价钱表类
 * @author JackieL&EvanC
 *
 */
public class BookPrice {
	
	/** 书籍id  */
	private int book_id;
	/** 书籍价钱 */
	private int money;
	public int getBook_id() {
		return book_id;
	}
	public void setBook_id(int book_id) {
		this.book_id = book_id;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	
}
